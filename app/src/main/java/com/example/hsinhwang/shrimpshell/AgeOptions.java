package com.example.hsinhwang.shrimpshell;

class AgeOptions {
    private String tvChild,spChildAge;

    public AgeOptions(String tvChild, String spChildAge) {
        this.tvChild = tvChild;
        this.spChildAge = spChildAge;
    }

    public String getTvChild() {
        return tvChild;
    }

    public void setTvChild(String tvChild) {
        this.tvChild = tvChild;
    }

    public String getSpChildAge() {
        return spChildAge;
    }

    public void setSpChildAge(String spChildAge) {
        this.spChildAge = spChildAge;
    }
}
